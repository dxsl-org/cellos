#![no_std]

use core::fmt::{Write, Result};

pub trait Framebuffer {
    fn width(&self) -> u32;
    fn height(&self) -> u32;
    fn set_pixel(&mut self, x: u32, y: u32, color: u32);
    
    fn clear(&mut self, color: u32) {
        for y in 0..self.height() {
            for x in 0..self.width() {
                self.set_pixel(x, y, color);
            }
        }
    }

    fn draw_rect(&mut self, x: u32, y: u32, w: u32, h: u32, color: u32) {
        for cy in y..(y + h) {
            for cx in x..(x + w) {
                self.set_pixel(cx, cy, color);
            }
        }
    }

    fn draw_char(&mut self, x: u32, y: u32, c: char, color: u32) {
        let code = c as usize;
        if code >= 256 { return; }
        let glyph = &FONT8X16[code];
        for row in 0..16 {
            let bits = glyph[row];
            for col in 0..8 {
                if (bits & (0x80 >> col)) != 0 {
                    self.set_pixel(x + col, y + row as u32, color);
                }
            }
        }
    }
}

pub struct GenericFramebuffer {
    addr: usize,
    width: u32,
    height: u32,
    bpp: u32,
}

impl GenericFramebuffer {
    pub unsafe fn new(addr: usize, width: u32, height: u32, bpp: u32) -> Self {
        Self { addr, width, height, bpp }
    }
}

impl Framebuffer for GenericFramebuffer {
    fn width(&self) -> u32 { self.width }
    fn height(&self) -> u32 { self.height }
    fn set_pixel(&mut self, x: u32, y: u32, color: u32) {
        if x >= self.width || y >= self.height { return; }
        let offset = (y * self.width + x) * (self.bpp / 8);
        unsafe {
            let ptr = (self.addr + offset as usize) as *mut u32;
            core::ptr::write_volatile(ptr, color);
        }
    }
}

pub struct TextConsole<'a, T: Framebuffer> {
    fb: &'a mut T,
    cursor_x: u32,
    cursor_y: u32,
    fg_color: u32,
    bg_color: u32,
}

impl<'a, T: Framebuffer> TextConsole<'a, T> {
    pub fn new(fb: &'a mut T) -> Self {
        Self {
            fb,
            cursor_x: 0,
            cursor_y: 0,
            fg_color: 0xFFFFFFFF, // White
            bg_color: 0x00000000, // Black
        }
    }

    pub fn set_colors(&mut self, fg: u32, bg: u32) {
        self.fg_color = fg;
        self.bg_color = bg;
    }

    pub fn putc(&mut self, c: char) {
        match c {
            '\n' => {
                self.cursor_x = 0;
                self.cursor_y += 16;
            }
            '\r' => {
                self.cursor_x = 0;
            }
            _ => {
                if self.cursor_x + 8 > self.fb.width() {
                    self.cursor_x = 0;
                    self.cursor_y += 16;
                }
                if self.cursor_y + 16 > self.fb.height() {
                    // Simple scroll: just clear and reset for now
                    self.fb.clear(self.bg_color);
                    self.cursor_y = 0;
                }
                self.fb.draw_char(self.cursor_x, self.cursor_y, c, self.fg_color);
                self.cursor_x += 8;
            }
        }
    }
}

impl<'a, T: Framebuffer> Write for TextConsole<'a, T> {
    fn write_str(&mut self, s: &str) -> Result {
        for c in s.chars() {
            self.putc(c);
        }
        Ok(())
    }
}

